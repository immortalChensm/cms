                各兄弟医院院长及有关领导、呼吸学科带头人：
                2016年8月14日，国家呼吸临床研究中心·呼吸专科医联体(以下简称“呼吸专联体”)成立大会在北京召开。来自全国各地的200余家医院的院长、呼吸与危重症医学科主任约500人参加了成立大会。
                呼吸专联体的成立，旨在促进呼吸疾病的分级诊疗、提升专科疾病预防、诊断和治疗水平，建立系统化、规范化的呼吸专科医师人才培养体系，推动学科发展。除牵头单位外，专科医联体在各地联合学术与技术实力较强的医院作为协作单位，与牵头单位共同实施技术辐射和双向转诊等工作。
                应各兄弟医院的要求，呼吸专科医联体欢迎更多有志于促进呼吸疾病分级诊疗、提升呼吸疾病预防、诊断和治疗水平的医院加入医联体，共同推动学科发展。针对有意愿加入的单位有如下几项条件及流程：
                1.原则是要求二级以上公立医院。
                2.医院设有呼吸学科和相关人才且呼吸科具备一定的规模，实力。
                3.提交申请表：
                1).请有意愿参与呼吸专科医联体的单位填写《呼吸专科医联体申请表》(见附件)及《调查问卷》(见附件)并将电子版发送至邮箱hxzkylt2016@126.com。
                2).打印《呼吸专科医联体申请表》及《调查问卷》加盖公章，并将图片或扫描件上传。
                附件：
                呼吸医联体申请表.docx
