            <div class="cont">
                <span>区域医联体(曾称“医院集团”)是很多单位已经做出的整合医疗资源的探索，有助于发挥医联体内优质医疗资源的作用，推动区域内常见、多发疾病的分级诊疗，提升基层单位的医疗能力与水平。但是，由于区域医联体内自身专科水平的限制，难于解决某专科疑难危重疾病的诊治问题。因此，区域医联体基本上属于“普及型”医联体，其覆盖范围亦仅限于局部地区。</span>
                <span class="nextxq">针对现阶段我国医疗资源总量不足，同时存在严重的碎片化、不均衡和非同质的状况，探索发挥优质专科资源的作用，在全国范围内提高对某专科疾病的诊治水平，为专科疑难危重患者提供便捷的就医途径，整体推动专科的建设与发展，中日医院提出建立专科医联体的构想：由在某一专科具有突出优势，居国内领衔地位的医院牵头，充分发挥其学科优势和技术辐射作用，集合国内相关医院，成立旨在促进专科疾病分级诊疗、推动学科发展的专科医联体。除牵头单位外，专科医联体在各地联合学术与技术实力较强的医院作为协作单位，与牵头单位共同实施技术辐射和双向转诊;邀请各级基层医院作为成员单位，帮助其解决专科医疗和学科建设问题。如此，可望有效统筹、整合、优化、合理配置专科医疗资源，分层级引导、安排专科疾病患者就医，将有限的优质医疗资源集中在最需要的疑难危重症诊疗上，使患者获得科学、便捷、经济、高效、连续的医疗照护。专科医联体属于“提高型”医联体，覆盖范围可以扩展至全国。</span>
